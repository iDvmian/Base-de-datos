---HACERLA CON ALIAS EN VEZ DEL NOMBRE COMPLETO, ESOT SE DEINE EN EL FROM

SELECT E.EMPLOYEE_ID AS COD_EMPELADO, CONCAT(E.FIRST_NAME, CONCAT(' ',E.LAST_NAME)) AS "NOMBRE COMPLETO",
 J.JOB_TITLE AS "CARGO", D.DEPARTMENT_NAME AS "DEPARTAMENTO"
FROM OEHR_EMPLOYEES E JOIN OEHR_JOBS J ON(E.JOB_ID =J.JOB_ID)
JOIN OEHR_DEPARTMENTS D ON (E.DEPARTMENT_ID = D.DEPARTMENT_ID);


------ DE EMPLOYEES A REGION (VUELTA LARGA )
--VER NO FUNCIONA
SELECT E.EMPLOYEE_ID AS "COD_EMPLEADO", E.LAST_NAME AS "APELLIDO_EMPLEADO", R.REGION_NAME AS "NOMBRE_CONTINENTE"
FROM OEHR_EMPLOYEES E JOIN OEHR_DEPARTMENTS D ON (E.DEPARTMENT_ID = D.DEPARTMENT_ID)
JOIN OEHR_LOCATION L ON (D.LOCATION_ID = L.LOCATION_ID)
JOIN OEHR_COUNTRIES CO ON (L.COUNTRY_ID = CO.COUNTRY_ID) 
JOIN OEHR_REGIONS R ON (CO.REGION_ID = R.REGION_ID);


