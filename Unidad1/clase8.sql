-----FORMA CORRECTA ULITILZADO JOIN
-- MI FORMA

SELECT EMPLOYEE_ID AS COD_EMPELADO, CONCAT(FIRST_NAME, CONCAT(' ',LAST_NAME)) AS "NOMBRE COMPLETO",
 JOB_TITLE AS "CARGO", DEPARTMENT_NAME AS "DEPARTAMENTO"
FROM OEHR_EMPLOYEES JOIN OEHR_JOBS ON(OEHR_EMPLOYEES.JOB_ID = OEHR_JOBS.JOB_ID)
JOIN OEHR_DEPARTMENTS ON (OEHR_EMPLOYEES.DEPARTMENT_ID = OEHR_DEPARTMENTS.DEPARTMENT_ID);


--- FORMA PROFE
--PONER SIMPRE EN TODAS LAS LAUSILAS DE DONDE ES EL ATRIBUTO DE LA TABLA


SELECT OEHR_EMPLOYEES.EMPLOYEE_ID AS "COD_EMPELADO", CONCAT(OEHR_EMPLOYEES.FIRST_NAME, CONCAT(' ',OEHR_EMPLOYEES.LAST_NAME)) AS "NOMBRE COMPLETO",
 OEHR_JOBS.JOB_TITLE AS "CARGO", OEHR_DEPARTMENTS.DEPARTMENT_NAME AS "DEPARTAMENTO"
FROM OEHR_EMPLOYEES JOIN OEHR_JOBS ON(OEHR_EMPLOYEES.JOB_ID = OEHR_JOBS.JOB_ID)
JOIN OEHR_DEPARTMENTS ON (OEHR_EMPLOYEES.DEPARTMENT_ID = OEHR_DEPARTMENTS.DEPARTMENT_ID);


