SELECT E.EMPLOYEE_ID AS "COD_EMPELADO", UPPER(CONCAT(E.FIRST_NAME, CONCAT(' ES ',CONCAT( J.JOB_ID, '_UCM_')))) AS "DATO CONCATENADO", J.JOB_TITLE AS "CARGO"
FROM OEHR_EMPLOYEES E JOIN OEHR_JOBS J ON (E.JOB_ID = J.JOB_ID);

