SELECT EMPLOYEE_ID, FIRST_NAME||' '||FIRST_NAME AS "NOMBRE COMPLETO", 
HIRE_DATE AS "FECHA DE CONTRATACION",
ROUND((SYSDATE - HIRE_DATE)/7) AS "SEMANAS TRABAJADAS"
FROM OEHR_EMPLOYEES
WHERE DEPARTMENT_ID IN (50,80)
ORDER BY "SEMANAS TRABAJADAS";
