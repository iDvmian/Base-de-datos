SELECT EMPLOYEE_ID, UPPER(LAST_NAME) AS "APELLIDO",
LENGTH(LAST_NAME) AS "CANTIDAD DE CARACTERES"
FROM OEHR_EMPLOYEES ORDER BY LENGTH(LAST_NAME);


--EJERCICIO 2

SELECT EMPLOYEE_ID, LAST_NAME AS "APELLIDO", ROUND(SALARY,-1)AS "REDONDEADO",
TRUNC(SALARY,-1) AS "TRUNCADO"
FROM OEHR_EMPLOYEES;


